    private void consultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaActionPerformed
        Connection connection = null;
        Statement statement = null;
        int linha = 0;
        try {
            Class.forName("org.sqlite.JDBC");
            connection = DriverManager.getConnection("jdbc:sqlite:Perguntas.db");
            connection.setAutoCommit(false);
            
            statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT USUARIO FROM RESPOSTA GROUP BY USUARIO");

           //Zera limpa a tabela grid
            DefaultTableModel mode = (DefaultTableModel) jTableConsulta.getModel();
            mode.setRowCount(0);
            
            while (resultSet.next()) {
                String usuario = resultSet.getString("USUARIO");
                
                //Lista os clientes na tabela (Grid)
                DefaultTableModel model = (DefaultTableModel) jTableConsulta.getModel();
                model.addRow(new Object[]{usuario});
                
                linha++;
                
            }
            resultSet.close();
            statement.close();            
            connection.close();
        } catch (Exception e) {
            System.err.println(e.getClass().getName() + "erro na consulta de pacientes bot√£o consultar " + e.getMessage());
            System.exit(0);
        }
    }//GEN-LAST:event_consultaActionPerformed

